public class TernaryOperator {
    public static void main(String[] args) {
        int a=2;
        int b=5;
        int min=(a<b)?a:b;
        System.out.println(min);
        System.out.println("-------");
        int x=10;
        int y=5;
        int minimum=(x<y)?x:y;
        System.out.println(minimum);
    }
}
