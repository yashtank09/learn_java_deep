package Basics.Operators;
public class AssignmentOperator {
    public static void main(String[] args) {
        int a=10;
        int b=20;
        a+=4;   //a=a+4 (a=10+4)
        b-=4;   //b=b-4 (b=20-4)
        System.out.println(a);
        System.out.println(b);
        System.out.println("-----------");
        int x=10;  
        x+=3;//10+3     
        System.out.println(x);
        x-=4;//13-4
        System.out.println(x);
        x*=2;//9*2
        System.out.println(x);
        x/=2;//18/2
        System.out.println(x);

    }
}
